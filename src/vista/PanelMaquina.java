    @Override
    @SuppressWarnings("empty-statement")
    public void update(Observable o, Object arg) {

        Apuntador apuntador = (Apuntador) arg;

        DefaultTableModel model = (DefaultTableModel) jTableCintaVisual.getModel();
        for (; model.getRowCount() > 0;) {
            model.removeRow(0);
        }

        for (int i = 0; i < cadena.length(); i++) {

            if (apuntador.getReferencia() != i) {
                String[] datos = {maquinita.getCinta().get(i), " ", String.valueOf(cadena.charAt(i))};
                model.addRow(datos);
            } else {
                String[] datos = {maquinita.getCinta().get(i), "<", String.valueOf(cadena.charAt(i))};
                model.addRow(datos);
            }

        }//

    }

