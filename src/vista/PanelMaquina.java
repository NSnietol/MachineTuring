    private void jButtonProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProcesarActionPerformed
        // TODO add your handling code here:

        jTextAreaResultado.setText("");
        // Validamos que los número sean binarios mediante la siguiente ER
        Pattern a = Pattern.compile("(1|0)(1|0){0,}");
        Matcher binario1Validar = a.matcher(jTextFieldNum1.getText());
        Matcher binario2Validar = a.matcher(jTextFieldNum2.getText());

        if (binario1Validar.matches() && binario2Validar.matches()) {

            if (jRadioButtonLento.isSelected()) {
                velocidad = 800;
            } else if (jRadioButtonMedio.isSelected()) {

                velocidad = 450;

            } else {
                velocidad = 150;
            }

            // Se valida que los número tenga la misma longitud, debido a que maquina solo procesa cuando son de igual longitud
            if (jTextFieldNum2.getText().length() != jTextFieldNum1.getText().length()) {
                int dif = jTextFieldNum2.getText().length() - jTextFieldNum1.getText().length();

                // Cuando se tiene dos números longitude distintas se llena con ceros a la izquiera para equilibrar
                String complemento = "";
                for (int i = 0; i < Math.abs(dif); i++) {
                    complemento += "0";
                }
                if (dif > 0) {

                    jTextFieldNum1.setText(complemento + jTextFieldNum1.getText());
                } else {

                    jTextFieldNum2.setText(complemento + jTextFieldNum2.getText());
                }

            }

            // La estructura de la cinta es LINUMEROINUMERO2IL
            cadena = CARACTERVACIO + CARACTERINTERMEDIO + jTextFieldNum1.getText() + CARACTERINTERMEDIO + jTextFieldNum2.getText() + CARACTERINTERMEDIO + CARACTERVACIO;

            Thread trabajador = new Thread() {
                @Override
                public void run() {
                    maquinita = new Maquina(cadena);
                    Apuntador referencia = new Apuntador(1);
                    referencia.addObserver(PanelMaquina.this);
                    jButtonProcesar.setEnabled(false);
                    
                    
                    ArrayList<String> toWork = maquinita.toWork(referencia);
                    
                    filtrar(toWork);
                    
                    jButtonProcesar.setEnabled(true);

                }

            };

            trabajador.start();
        } else {
            JOptionPane.showMessageDialog(this, "Recuerde que debe escribir números binarios en los campos");
        }


    }//GEN-LAST:event_jButtonProcesarActionPerformed

    public boolean filtrar(ArrayList<String> cinta) {
         jTextAreaResultado.setText(cinta.toString());
        
        // La cinta tiene LIIIIIRESULTADOL, entonces no leemos ni la primera ni la última posición
        String result = jTextFieldNum1.getText()+"+"+jTextFieldNum2.getText()+" = ";
        for (int i = 1; i < cinta.size() - 1; i++) {

            if (!cinta.get(i).equals(CARACTERINTERMEDIO)) {
                result += cinta.get(i);
            }
        }
        
        
        jTextAreaResultado.setText(jTextAreaResultado.getText() + "\n\n" + result);

        return true;
    }


